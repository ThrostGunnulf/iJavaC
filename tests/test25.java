class test25
{
	static int[] a1, a2;
	static boolean[] b1, b2;
	
	public static void main(String[] args)
	{
		a1 = new int[3];
		a2 = new int[0];
		
		b1 = new boolean[4];
		b2 = new boolean[0];
		
		System.out.println(a1.length);
		System.out.println(a2.length);
		
		System.out.println(b1.length);
		System.out.println(b2.length);
	}
}
